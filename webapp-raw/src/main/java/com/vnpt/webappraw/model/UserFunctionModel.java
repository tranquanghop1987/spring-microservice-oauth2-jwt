package com.vnpt.webappraw.model;

public class UserFunctionModel {
	private Long userFunctionId;
	private String description;
	private String userFunctionName;
	public Long getUserFunctionId() {
		return userFunctionId;
	}
	public void setUserFunctionId(Long userFunctionId) {
		this.userFunctionId = userFunctionId;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getUserFunctionName() {
		return userFunctionName;
	}
	public void setUserFunctionName(String userFunctionName) {
		this.userFunctionName = userFunctionName;
	}

}
